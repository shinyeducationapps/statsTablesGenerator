        to cite the developer of the app and without fear of copyright 
        infringement. </b>"
      ),
      br(),
      br(),
      
      # contact info
      h4("Contact"),
      p("Email: Anonymous"),
      br(),
      br(),
      
      # copyright statement
      p("Copyright \uA9 2020-2021 by Anonymous."),
